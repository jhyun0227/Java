package ch02;

public class OperTest2 {
	public static void main(String[] arg) { 
		String str="관계(비교)연산자 예제 입니다.";
		int x=10; int y=5;
		System.out.println(str);
		System.out.println("x = " + x + " y = " + y);
		System.out.println("[x >= y] = " + (x>=y));
		System.out.println("[x<y] = " + (x<y));
		System.out.println("[x<=y] = " + (x<=y));
		System.out.println("[x==y] = " + (x==y));
		System.out.println("[x!=y] = " + (x!=y));
	}
}
